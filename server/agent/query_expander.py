# 常见词，但不够具体，不进行扩展
COMMON_WORDS = {"工作", "岗位", "活", "兼职", "全职"}


def expand(text: str) -> list[str]:
    """
    扩展用户输入，返回应该搜索的关键词列表。

    参数:
        text: 用户输入文本

    返回:
        [keyword1, keyword2, ...] 去重后返回
    """
    keywords = set()
    text_lower = text.lower()

    for word, expansions in JOB_SYNONYMS.items():
        if word in text_lower:
            for exp in expansions:
                keywords.add(exp)

    # 如果没有任何扩展，用原文中的双字词作为兜底
    if not keywords:
        for i in range(len(text) - 1):
            bigram = text[i:i + 2]
            if bigram not in COMMON_WORDS:
                keywords.add(bigram)

    return list(keywords)


def expand_preferences(job_preferences: list[str]) -> list[str]:
    """对用户的所有工种偏好进行批量扩展"""
    all_keywords = set()
    for pref in job_preferences:
        expanded = expand(pref)
        for kw in expanded:
            all_keywords.add(kw)
        all_keywords.add(pref)  # 保留原词
    return list(all_keywords)
